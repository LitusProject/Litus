namespace CommonBundle\Repository\General;

use CommonBundle\Component\Doctrine\ORM\EntityRepository;

/**
 * Visit
 *
 * This class was generated by the Doctrine ORM. Add your own custom
 * repository methods below.
 */
class Visit extends EntityRepository
{
    public function findAllByControllerQuery($controller)
    {
        $query = $this->_em->createQueryBuilder();
        $resultSet = $query->select('v')
            ->from('CommonBundle\Entity\General\Visit', 'v')
            ->where(
                $query->expr()->like(
                    $query->expr()->lower('v.controller'),
                    ':controller'
                )
            )
            ->setParameter('controller', strtolower($controller) . '%')
            ->orderBy('v.timestamp', 'DESC')
            ->getQuery();

        return $resultSet;
    }

    public function findAllByUserQuery($name)
    {
        $query = $this->_em->createQueryBuilder();
        $resultSet = $query->select('v')
            ->from('CommonBundle\Entity\General\Visit', 'v')
            ->innerJoin('v.user', 'p')
            ->where(
                $query->expr()->orX(
                    $query->expr()->like(
                        $query->expr()->concat(
                            $query->expr()->lower($query->expr()->concat('p.firstName', "' '")),
                            $query->expr()->lower('p.lastName')
                        ),
                        ':name'
                    ),
                    $query->expr()->like(
                        $query->expr()->concat(
                            $query->expr()->lower($query->expr()->concat('p.lastName', "' '")),
                            $query->expr()->lower('p.firstName')
                        ),
                        ':name'
                    )
                )
            )
            ->setParameter('name', '%' . strtolower($name) . '%')
            ->orderBy('v.timestamp', 'DESC')
            ->getQuery();

        return $resultSet;
    }

    public function findAllByUrlQuery($url)
    {
        $query = $this->_em->createQueryBuilder();
        $resultSet = $query->select('v')
            ->from('CommonBundle\Entity\General\Visit', 'v')
            ->where(
                $query->expr()->like(
                    $query->expr()->lower('v.url'),
                    ':url'
                )
            )
            ->setParameter('url', '%' . strtolower($url) . '%')
            ->orderBy('v.timestamp', 'DESC')
            ->getQuery();

        return $resultSet;
    }
}
