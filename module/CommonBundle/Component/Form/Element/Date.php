namespace CommonBundle\Component\Form\Element;

use CommonBundle\Component\Form\ElementInterface;
use CommonBundle\Component\Form\ElementTrait;

/**
 * Date form element
 *
 * @author Bram Gotink <bram.gotink@litus.cc>
 * @author Pieter Maene <pieter.maene@litus.cc>
 */
class Date extends \Zend\Form\Element\Date implements ElementInterface
{
    use ElementTrait;

    /**
     * @var string
     */
    protected $format = 'd/m/Y';

    /**
     * @return void
     */
    public function init()
    {
        parent::init();

        if (!$this->hasAttribute('placeholder')) {
            $this->setAttribute('placeholder', 'dd/mm/yyyy');
        }
    }
}
