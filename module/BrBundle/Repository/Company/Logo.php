namespace BrBundle\Repository\Company;

use BrBundle\Entity\Company as CompanyEntity,
    CommonBundle\Component\Doctrine\ORM\EntityRepository;

/**
 * Logo
 *
 * This class was generated by the Doctrine ORM. Add your own custom
 * repository methods below.
 */
class Logo extends EntityRepository
{
    public function findAllByCompanyQuery(CompanyEntity $company)
    {
        $query = $this->_em->createQueryBuilder();
        $resultSet = $query->select('l')
            ->from('BrBundle\Entity\Company\Logo', 'l')
            ->where(
                $query->expr()->eq('l.company', ':company')
            )
            ->setParameter('company', $company)
            ->getQuery();

        return $resultSet;
    }

    public function findOneByTypeAndCompany($type, CompanyEntity $company)
    {
        $query = $this->_em->createQueryBuilder();
        $resultSet = $query->select('l')
            ->from('BrBundle\Entity\Company\Logo', 'l')
            ->where(
                $query->expr()->andX(
                    $query->expr()->eq('l.company', ':company'),
                    $query->expr()->eq('l.type', ':type')
                )
            )
            ->setParameter('company', $company)
            ->setParameter('type', $type)
            ->setMaxResults(1)
            ->getQuery()
            ->getOneOrNullResult();

        return $resultSet;
    }

    public function findAllByTypeQuery($type)
    {
        $query = $this->_em->createQueryBuilder();
        $resultSet = $query->select('l')
            ->from('BrBundle\Entity\Company\Logo', 'l')
            ->where(
                $query->expr()->eq('l.type', ':type')
            )
            ->setParameter('type', $type)
            ->getQuery();

        return $resultSet;
    }
}
