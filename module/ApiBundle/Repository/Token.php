namespace ApiBundle\Repository;

use ApiBundle\Entity\Code\Authorization as AuthorizationCode;
use DateTime;

/**
 * Token
 *
 * This class was generated by the Doctrine ORM. Add your own custom
 * repository methods below.
 */
class Token extends \CommonBundle\Component\Doctrine\ORM\EntityRepository
{
    /**
     * @param  AuthorizationCode $authorizationCode
     * @return array
     */
    public function findAllActiveByAuthorizationCode(AuthorizationCode $authorizationCode)
    {
        $query = $this->getEntityManager()->createQueryBuilder();
        return $query->select('t')
            ->from('ApiBundle\Entity\Token', 't')
            ->where(
                $query->expr()->and(
                    $query->expr()->gt('t.expirationTime', ':now'),
                    $query->expr()->gt('t.code', ':code')
                )
            )
            ->setParameter('now', new DateTime())
            ->setParameter('code', $authorizationCode)
            ->getQuery()
            ->getResult();
    }
}
