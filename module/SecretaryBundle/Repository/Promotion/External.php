    public function findOneByEmailAndAcademicYear($email, AcademicYear $academicYear)
    {
        $query = $this->getEntityManager()->createQueryBuilder();
        $resultSet = $query->select('e')
            ->from('SecretaryBundle\Entity\Promotion\External', 'e')
            ->where(
                $query->expr()->andX(
